	<h1 class=" text-lg font-semibold">Torstens levnadsbeskrivning</h1>
	<p class="my-4">Min levnadsbeskrivning.</p>
	<p>Påbörjad tisdagen den 4 februari år 2003 kl. 17.30</p>
	<p class="my-4">Jag har just tagit en skidtur efter en natts och en dags snöande.</p>
	<p class="my-4">
		För att få lite bakgrund till beskrivningen börjar jag år 1914 dvs det år, då både Mamma och
		Pappa, helt oberoende av varandra, kom till Adelöv. Mamma kom tillsammans med sina föräldrar
		August och Emma Johansson och syskonen Sture och Sigrid. Mamma var född år 1901 och yngst av
		syskonen. Morfar hade tidigare arrenderat Tranberga gård i Torpa socken i Ydre. Han hade nu
		tagit Adelövsgård på arrende från den 14 mars 1914. Mamma berättade om flyttningen att Morfar
		både fått hjälp av grannar, vänner och släktingar med flytten. Eftersom de hade med sej både
		yttre inventarier, levande inventarier dvs alla djur samt inre inventarier dvs möbler, husgeråd
		m.m., sa gick det åt många hästskjutsar. Flyttningen genomfördes på en dag och men gjorde
		uppehåll i Säbydal, som ligger bredvid Säby kyrka. Morfar hade sitt hem i Äpplaryd ovanför
		Säbydal, och där fanns nu hans mamma och en syster. Dessa hade lagat mat och kört ner till
		Säbydal, sa att alla fick ett mål mat under dagen. Djuren fick foder, och vatten kunde de dricka
		i Svartån. När de kom fram till Adelövsgård gällde det för Mormor och flickorna att tända i
		spisar och att laga mat. De både packat allt husgeråd överst på en vagn, sa det var
		lättåtkomligt. Det blev säkert sen kväll innan allt var klart. Alla djur skulle in i ladugården
		och få vatten och foder, korna skulle mjölkas och allt folket skulle få mat och någonstans att
		ligga.
	</p>
	<p class="my-4">
		Pappas flyttning till Adelöv var betydligt enklare. Han hade haft sin första lärartjänst i
		Hyltan, som ligger sydost om Säbysjön i Säby socken. Han flyttade med sitt bohag, som jag
		förmodar inte var så stort. Kanske fick han hjälp med det av några grannar, jag vet inte. Han
		tyckte i alla fall att han både kommit till en ny och fin skola. Jag tror skolhuset var byggt år
		1905, sa det var bara nio år gammalt. Han fick nu också en ny typ av tjänst dels som lärare och
		dels som kantor. Han var 27 år, när han kom till Adelöv. Eftersom han inte var gift skaffade han
		sej hushållerska. Hon hette Hulda Andersson och både dessförinnan haft någon annan
		hushållstjänst. Hon var kvar hos pappa tills han gifte sej 1927. Hon är begravd på Adelövs
		kyrkogård i sydöstra delen av kyrkogården innanför gången. Pappa tog lärarexamen i Linköping
		1910. Sedan han kom till Adelöv genomgick han utbildning till och tog organistexamen under en
		eller flera somrar i Visby.
	</p>
	<p class="my-4">
		Jag föddes den 1 februari 1929 c:a kl. 21.30. Jag föddes hemma i skolhuset i Adelöv i sovrummet
		på andra våningen, dvs. den våningen, som nu är borttagen. Barnmorskan Anna Andersson från
		Kvarnarp var där och hjälpte till. Det lär ha varit en kall dag. Jag var säkert efterlängtad
		särskilt av Pappa, som var närmare 42 år, när jag föddes.
	</p>
	<p class="my-4">
		De första åren kommer man ju inte ihåg någonting av, men jag minns att det kändes sa högt, när
		Mamma bar mej. Jag minns också att jag sov middag och att jag sa småningom begrep att det inte
		varit någon natt sedan jag varit vaken sist. Ett annat minne, som jag har sedan tidig barndom är
		av någon underlig anledning kattögat på Mammas cykel. Det var ett sådant där gammaldags kattöga,
		som stod ut, och det lyste så vackert, när solen sken på det. Förmodligen satt det i jämnhöjd
		med mina ögon. Det fanns cykelstall i södra entrén till skolan. Där fanns tre cykelstall, ett
		för Pappas cykel, ett för Mammas och ett för fröken Larssons. Fröken Larsson var
		småskollärarinna.
	</p>
	<p class="my-4">
		Jag hade förstås en väldigt omhuldad uppväxt och var säkert bortskämd. Mamma var ju hemma, Pappa
		arbetade i samma bus, och det fanns alltid någon jungfru hemma. Särskilt minns jag Margit Ax,
		numera Pettersson. Hon lekte med mej, när hon både tid till det och det tyckte jag förstås var
		väldigt roligt. Vi både telefonstation hemma, och den var öppen mellan kl. 7 på morgonen och 9
		på kvällen. Den behövde ju ständig passning, sa därför fanns det alltid någon hemma. Ibland
		hände det att folk ringde och frågade om rätt tid, för de räknade med att på telefonstationen
		skulle det finnas en rättvisande klocka. På den tiden sade man ju alltid att klockan var t.ex. 9
		minuter över 8. Men Margit, som var en spjuver svarade då att klockan var 8. 9. och 10. Jag vet
		inte om vederbörande förstod vad hon menade. På kvällarna spelade vi ofta fia vid telefonväxeln.
		Efter växelns stängning kunde man också ringa, men då kostade det extra. Det fanns då en extra
		larmklocka, som hördes, för man behövde inte vara vaken och passa telefonen för att ta emot
		eventuella samtal.
	</p>
	<p class="my-4">
		Före jul köptes det alltid del i gris och det lagades förstås extra mycket mat. Särskilt minns
		jag att det gjordes korv. Som jag kommer ihåg det, sa var det två sorters korv: grynkorv och
		skallekorv. Vad var nu detta för korv? I grynkorv vet jag att det fanns bl.a. korngryn och
		fläskfärs och nötfärs, och div. kryddor. Smeten stoppades i fjälster dvs. svintarm, och det
		måste ha varit av tjocktarmen, för det var verkligen tjocka korvar. Tarmarna krävde ju noggrann
		rengöring och skulle ligga i vatten före användning. Ibland var Astrid Hermansson i Ryd och
		hjälpte oss då. Hon skojade alltid med mej och henne tyckte jag mycket om. Till julen skulle det
		förstås städas överallt och göras fint. Det skulle också baras in ved, sa att vi inte behövde
		göra det under juldagarna. Det tog ganska lång tid, för dels både vi tre kakelugnar att elda i
		och köksspisen och dels skulle ju veden upp en trappa. Vedförrådet fanns i södra andan av de
		sedan länge rivna kyrkstallarna på andra sidan vägen. Julgran kunde vi ta på Adelövsgård, men
		ibland fick vi granen hemburen. Om jag var med och hämtade granen både jag sällskap antingen med
		Morfar eller med Morbror Sture.
	</p>
	<p class="my-4">
		När skolan hade slutat före jul brukade Mamma, Pappa och jag åka till Jönköping och titta på
		julskyltning. Vi låg då också över natt i Jönköping och handlade på måndagen. Att titta på
		julskyltningen tyckte jag var roligt, men att gå och handla dagen efter uppskattade jag inte.
		Jag har lite svart att förstå, att Mamma och Pappa kostade på sej denna extravagans, det var
		säkert Mammas förslag. Av julskyltningen minns jag att det var många saker, som rörde sej. I en
		affär, jag tror det var i Sidenhuset, minns jag att det var en flicka eller dam, som dök upp ur
		en fint klädd tunna. I många affärer var det tomtar som kom fram och visade saker. Det fanns en
		leksaksaffär, som hette Albins, och där fanns det många rörliga saker.
	</p>
	<p class="my-4">
		Men vi både ju också affär på närmare håll nämligen Ingessons affär, som vi var granne med. Där
		var det också julskyltning, och jag minns att det var någon lantbrukare, som körde fram med hela
		sin familj och beskådade hela härligheten En kväll före jul brukade vi få gå dit och köpa frukt
		och nötter m.m. och sedan var vi uppbjudna till Vasti och Simon Ingesson på kaffe
	</p>
	<p class="my-4">
		I skolan brukade det på julavslutningen vara någon eller några pjäser. Jag minns att vi en gång
		spelade "Ruskaby skola". Till julavslutningen brukade Pappa och några av flickorna i skolan, som
		var duktiga att rita, alltid rita tomtar och julgranar m.m. Detta fick stå kvar på tavlan tills
		skolan började igen. Ibland gick jag ner och tittade på detta och tyckte att det var fint.
	</p>
	<p class="my-4">
		På Luciaafton dvs den 12 december kom det alltid "lussegubbar" på kvällen. De var utklädda, sa
		att ingen skulle känna igen dem, och det var förstås en sport att ändå kunna känna igen en del.
		De hade "skrapansikten" på sej, vilket var hemmagjorda masker. Det var för det mesta drängar
		från gårdarna i bygden, som kom. I början av 30-talet var det mycket arbetslöshet, och då fanns
		det s.k. AK-arbetare (AK= arbetslöshetskommissionen) Dessa gick också lussegubbar, och dem var
		det omöjligt att känna igen. Alla undfägnades med pepparkakor och kanske kaffe, jag minns inte
		riktigt. Innan de kom in brukade de slå i väggarna, men jag kan aldrig minnas att det var något
		brak. Jag minns än den där skräckblandade glädjen jag kände, när de kom, för då visste man ju
		att det inte var så långt kvar till jul. På Luciadagen brukade Mamma och någon mera komma och
		lussa, och det var förstås högtidligt. Någon gång gick de också in till Ingessons och lussade.
		Då minns jag att jag stod i fönstret upp hos oss och såg att de gick med Luciakronan på sej.(Det
		kunde val inte ha blast sa mycket då.)
	</p>
	<p class="my-4">
		Dagen före julafton skulle det förstås kläs med julsaker och prydnader överallt. Vi hade många
		pappersbonader, som sattes upp, och i en del rum var det girlanger i taket. I kökstaket sattes
		det upp rött och vitt crepepapper i långa längder, som var snodda om vartandra. På 30- talet
		hade vi ingen elektricitet, sa då hade vi stearinljus i granen. Det var ju vackert, när det
		brann, men synnerligen eldfarligt. Särskilt, som det var fullt av papperskarameller i granen.
		Till alla ljusstakar, som skulle stå har och där gjordes det ljusmanschetter av silkespapper.
		Man använde rött och vitt papper, som klipptes på ett särskilt satt och sedan virades runt
		ljusen.
	</p>
	<p class="my-4">
		På julafton skulle det sättas ljus i kyrkan. Då var hela den s.k. kyrkobetjäningen med. Den
		bestod av kyrkoherden, två ringkarlar, som ibland kallades dödgrävare, kyrkvaktmästare,
		orgeltrampare, två kyrkvärdar och så Pappa. Först skulle alla hjälpas åt med att sätta i ljusen,
		som Morfar hade tagit hem, eftersom han var kyrkokassör. Sedan skulle alla ha kaffe hos oss. Då
		fick också alla ett paket ljus och en påse med något i kanske karameller och lussekatter. Jag
		minns att Mamma inte var särskilt förtjust över att ha alla dessa på kaffe på julafton, och det
		kan man ju förstå.
	</p>
	<p class="my-4">
		Innan ljussättningen i kyrkan gick av stapeln hade vi förstås ätit frukost. På julafton skulle
		den innehålla bl.a. grynkorv och säkert andra saker också, som jag inte kommer ihåg nu. Som alla
		andra barn tyckte jag förstås att tiden gick långsamt på julafton. Då kunde jag ibland få åka
		iväg med en julblomma till någon, och det var ju bra, för på julafton fick man alltid något dit
		man kom. Vi doppade i grytan vid ett-tiden, och då var också Mormor och Morfar med. Jag minns
		att särskilt Morfar tyckte mycket om att doppa i grytan Efter doppet skulle vi gå till
		Ålderdomshemmet, för där skulle det vara en andakt. Pappa skulle ju vara med och sjunga, och
		Mamma och jag var med också. Kyrkoherden var också med. Alla på Ålderdomshemmet fick varsin
		julpåse, och sa bjöds det på kaffe. Sedan gick vi hem, och då kunde den "riktiga" julen börja -
		nästan. För när vi kom hem från Ålderdomshemmet gick vi oftast till Ingessons och önskade God
		jul, först till Viktoria, som bodde på nedre botten. Då hade hennes bror Valfrid, som bodde i
		Stockholm kommit hem. Han kom dagen före julafton eller på julafton. En gång hade han grapefrukt
		med sej hem, som vi fick smaka på, men det tyckte jag verkligen inte var gott. Han var snickare
		och ensamstående och kom bara hem en gång om året. Vi fortsatte sedan till övre våningen, där
		Vasti och Simon och Åke Ingesson bodde. Där bodde också en svenskamerikan Carl Danielsson, som
		hyrde ett rum. Han hade varit i Kalifornien ett antal år, men återvänt till hembygden. Han var
		morbror till Simon Ingesson och hans syster Viktoria. Sedan gick vi hem. Då var det kaffe igen
		och då var Morfar och Mormor och moster Alice och Morbror Sture med också med. Dessutom var det
		alltid en jungfru med, sa vi var en sju, åtta stycken. Sedan var det julklappsutdelning, vilket
		förstås var höjdpunkten. Det var ofta en tomte, som skötte den, men på senare tid fick jag göra
		det.
	</p>
	<p class="my-4">
		Vad kunde man få för julklappar då? Ja jag minns att jag fick leksaksbilar, en tvål, som såg ut
		som en gubbe, näsdukar, skidor, ångmaskin, meccano, luftgevär och förstås mycket annat.. Morfar
		var mycket tekniskt intresserad, sa vi kunde köra med ångmaskinen eller skruva med meccanot.
		Till luftgeväret var det pilar med och en piltavla, som vi ställde upp emot en dörr. Morfar,
		morbror Sture, Pappa och jag skot med luftgeväret.
	</p>
	<p class="my-4">
		Ibland var vi hos Mormor och Morfar på julafton, och då gick till på samma sätt som hemma. En
		julafton var vi i Adelövsgård på julafton. Det var år 1943, och då hade Mormor dött strax före
		julafton. Hon "stod lik" i det s.k. Postrummet i uthuset bakom Västerby. Om det var därför vi
		var i Adelövsgård då vet jag inte.
	</p>
	<p class="my-4">
		På juldagsmorgonen skulle vi förstås till julottan, som var klockan sex. Innan dess hade vi ätit
		lite frukost. Något som jag kommer ihåg från dessa morgnar var att men kunde stå i salsfönstret,
		som vette åt söder och se upp mot Klocklyckebacken, där det kom många bilar och en del
		hästskjutsar med bloss. Pappa, som skulle spela i kyrkan gick i god tid, och ibland gick jag med
		honom. Då tog vi vägen förbi Ingessons, för att se att det lyste i alla fönster. I julottan var
		det alltid mycket folk. Jag brukade träffa Bengt Atlof och vi kunde berätta vad vi fått i
		julklapp.
	</p>
	<p class="my-4">
		Juldagen var en skön dag för Pappa, för då kunde han koppla av efter julottan. Jag kunde ju ägna
		mej åt minajulklappar.
	</p>
	<p class="my-4">
		Efter jul var det mycket kafferep och ett och annat kalas. Ibland var vi på kalas redan på
		juldagen. Det var hos syskonen Andersson i Ulfsbo, som ligger i norra delen av Adelöv utefter
		vägen till St. Åby. Syskonen var ogifta och hette August, Alfred, Matilda och Hilma. Det var som
		sagt många kafferep, men om det inte fanns några barn dit vi var bjudna var jag helst hemma.
		lbland hade vi våra släktingar på julkalas, och ibland var vi hos dem. Jag minns att vi alltid
		var i Uppgränna hos moster Sigrid och morbror Frithiof och på Näs i Gränna socken hos faster
		Sigrid och farbror David.
	</p>
	<p class="my-4">
		Vintern var en skön tid, och jullovet var långt. Efter jul var det nämligen fortsättningsskola i
		sex veckor, och då var barn i första till sjätte klass lediga. Fortsättningsskolan var för de
		barn, som gått sina sex år i skolan, och som då fick en påbyggnad. Alla gick två sådana
		sexveckorskurser i två år efter varandra. Det kallades jordbruksbetonad fortsättningsskola, och
		där lästes om ett jordbruks skötsel. Det ingick också byggnadsritning, vilket jag tror, att alla
		tyckte var väldigt roligt.
	</p>
	<p class="my-4">
		För flickorna började man med skolkök runt år 1940, men vilken undervisning flickorna hade
		dessförinnan vet jag ej. För att fortsatta med skolköksundervisningen, så bedrevs den under
		sommaren. Den var nog väldigt primitiv, för dels höll man till i vårt brygghus, (tvättstuga)och
		dels sattes det in en järnspis i slöjdsalen för träslöjd, där man då också höll till.
		Skolkökslärarinnorna bodde i det s.k. vikarierummet, som fanns på samma våningsplan som var
		lägenhet. De var mycket tillsammans med oss, och återkom ju ofta år efter år.
	</p>
	<p class="my-4">
		Vintrarna 1939 till 1943 var mycket kalla, och åtminstone under ett par av vintrarna frös
		vattnet i skolhuset. Då fick vi hämta vatten i Adelövsgård och dra det i 50 liters mjolkflaskor
		på kälke. Jag minns att det någon gång var 39 grader kallt. Vid sådan kyla drar träet ihop sej,
		och då small det i träväggarna i skolhuset. Det gick förstås åt mycket ved att elda med. På
		skolgården kunde vi på vårsidan bygga snöfästningar. När sedan all sno smälte rann mycket
		smältvatten ned över skolgården. Då minns jag att Simon Ingesson hjälpte Åke Ingesson och mej
		att göra dammar och vattenhjul.
	</p>
	<p class="my-4">
		Vid påsken, narrnare bestämt på påskafton eldade vi påskeld, och det var förstås för att skrämma
		bort alla påskkäringar. Jag kan inte minnas att vi barn gick omkring som barn gör idag utklädda
		till påskkäringar. På-påskaftonen åt vi påsmat ungefär som idag, och ibland var det någon granne
		inbjuden, ofta Fritzells. Jag fick påskägg med godis i. På långfredagen fick jag inte leka sa
		mycket.
	</p>
	<p class="my-4">
		Valborgsmässoafton firades till att börja med inte sa mycket. På 40-talet började man fira med
		brasa, och jag minns att kyrkokören sjöng.
	</p>
	<p class="my-4">
		Någon gång under 1930-talet fick Pappa en sommarstugetomt i Örserumsbrunn, och där byggdes då en
		sommarstuga. Där bodde vi några veckor på somrarna, men jag trivdes nog bättre i Adelöv. Pappas
		syster Lisa fick också en tomt i Örserumsbrunn inte sa långt från var. När Arne och Uno var där
		var det roligt att vara där nere. lbland lekte vi indianer i skogen och ibland tog vi saft och
		bullar med oss och rodde bort till stora badstranden.
	</p>
	<p class="my-4">
		Jag slutade skolan i Adelöv, när jag gick ut 6:e klass våren 1941. Hösten 1941 började jag på
		realskolan i Tranås, och gick sedan där i fyra år. På den tiden fick man tentera in på
		realskolan, och det gick till sa att jag bodde i Tranås på Geijers Pensionat under de dagar, som
		intagningsproven pågick. På Geijers Pensionat bodde jag sedan inackorderad under alla fyra åren
		i Tranås. Innehavarna av pensionatet hette Ester och Carl O Welander och de hade varit
		missionärer i Brasilien under många år. De hade en son, som hette Arne, som gick i samma klass i
		realskolan som jag. Vi delade rum alla åren, och det gick bra, fast ibland var vi osams, men det
		gick bättre med tiden. Bussförbindelserna till Adelöv var inte sådana att de passade med
		skoltiderna och därför fick jag bo inackorderad. Det betydde att jag kom hem från Tranås kl.
		15.30 på lördagen och åkte tillbaka kl. 15.30 på söndagen. Det var förstås roligt att komma hem
		på lördagen och mindre roligt att åka tillbaka på söndagen. Under höst och vår hände det att vi
		cyklade hem och tillbaka. Det var oftast Bengt Athlof, som började ett år efter mej på
		realskolan, som jag hade sällskap med.
	</p>
	<p class="my-4">
		Innan jag kommer till åren i Tranås vill jag berätta lite mer om släktingarna, som bodde i
		Adelöv. Det var förstås i första hand Mormor och Morfar, som bodde tvärs över vägen i det hus,
		som kallas Västerby. Jag var ofta där, särskilt när Mormor hade gjort pannkaka. Det var inte
		tunna pannkakor utan pannkaka gjord på ostlöpe i bunke. Till pannkakan fick jag hallonsylt.
		Ibland, när Mamma och Pappa var borta på kvällen var jag i Västerby. Det hände någon gång att
		Morfar läste för mej, alltid ur samma bok. Han hade en särskild rost, när han läste, som jag
		ännu kommer ihåg. Morfar var mycket tekniskt intresserad och ibland experimenterade vi med
		ficklampor. Vi satt då nästan alltid i köket, som var det enda rum, som var upplyst(med
		fotogenlampa fram tilljulen 1939). Det fanns två fotogenlampor i köket en i taket över matbordet
		och en mindre ,som hängde vid spisen. Mormor var inte riktigt frisk och orkade inte så mycket.
		Hon hade en gång blivit påkörd i Klocklyckebacken, och tydligen repade hon sej inte riktigt
		efter den olyckshändelsen. Hon dog år 1943 precis före jul, sa hon "stod lik" i ett rum i
		uthuset vid Västerby. Morfar sov under nätterna fram till begravningen hos oss, men sedan sov
		han i Västerby igen. Han hyrde dock ut övervåningen i Västerby, och maten åt han sedan hos oss.
	</p>
	<p class="my-4">
		Förutom Mormor och Morfar hade jag också moster Alice och morbror Sture, som bodde i
		Adelövsgård. Jag tror jag kan saga att mitt största nöje under uppväxten var att vara på
		Adelövsgård. Där hände det ju alltid något, och alltid fanns det folk någonstans. Att få vara
		med och le kor på våren var ett stort äventyr. I höskörd och spannmålsskörd fick jag åka med i
		lassen och känna doften av torkat hö eller av havre eller råg. Det stacks alltid, när man åkte i
		lassen särskilt om det var råg och om det fanns tistel i. Att sedan när jag blev lite större få
		kö köra hast själv var förstås roligt. Det hände någon gång att jag fick köra parhästar, vilket
		ju var lite extra spännande. En gång körde vi gödsel till en åker halvvägs till Nostorp. När det
		var avlastat ställde jag mej på kärran för att köra till kanten och sätta på "fjölerna" dvs
		sidolämmarna. Under körningen till kanten körde jag snett över en slutfåra och då gungade
		gödselkärran till och jag slant av och tappade också tömmarna. Det kunde ju gått sa att hästarna
		skenat, men det gjorde de inte. Jag fick i alla fall en åthutning för att jag körde snett över
		slutfåran.
	</p>
	<p class="my-4">
		År 1945 tog jag realexamen i Tranås. Det var förstås en stor dag med mycket blommor, jag minns
		inte sa mycket mera av den dagen.
	</p>
	<p class="my-4">
		Eftersom det inte fanns något gymnasium i Tranås fick jag i augusti samma år börja på gymnasiet
		i Eksjö. Jag fick förstås bo inackorderad där också, eftersom det var längre hemifrån än till
		Tranås. Jag hann med att bo på tre olika ställen under tiden i Eksjö. Mat åt jag hos en fru
		Klasson, som hade matställe för gymnasister. På den tiden fanns det ju ingen mat på skolorna.
		Rektorn på skolan hette Johan Nilsson och han var en mycket respektingivande person., som man
		inte opponerade sej emot. Han var halvbror till Fritiof Nilsson-Piraten. Jag märkte dock aldrig
		att han hade någon humor.
	</p>
	<p class="my-4">
		Under gymnasietiden hade jag särskilt en kamrat, som hette Per Ljunggren. Han var från
		Oskarshamn och bodde liksom jag inackorderad. Vi brukade vara ute och cykla och ibland gick vi
		på bio. Efter studentexamen kom han in på befälsutbildning vid flottan. Han omkom tyvärr första
		hösten i en olycka på en av flottans båtar.
	</p>
	<p class="my-4">
		Studentexamen i Eksjö blev ett kapitel för sej, eftersom jag körde i examen första gången. På
		den tiden gick det till sa att det först var en skrivningsperiod, som sedan följdes av muntlig
		tentamen någon månad senare i närvaro av censorer. Jag hade dåligt resultat på skrivningarna och
		skulle naturligtvis inte ha släppts upp i muntlig tentamen. Nu var jag i alla fall med på den
		muntliga tentamen och det gick inte bra det heller. När kollegiet tillsammans med censorerna
		hade bestämt att jag inte blev godkänd kom det en lärare och hämtade mej och sade som det var.
		Då fick jag gå ut bakvägen, och jag minns att Pappa och Mamma kom och mötte mej och vi gick hem
		till mitt inackorderingsställe. Där var det dukat till fest och det blev någon slags fest, men
		det var naturligtvis något avslaget. Många grannar från Adelöv och släktingar hade kommit, sa
		dom fick mat i alla fall. Jag tror att det kändes värre för Mamma och Pappa än för mej. För
		Pappa var studentexamen något, som han aldrig hade fått ta och han såg nog fram emot att jag
		skulle klara det. Jag gick sedan om sista året, och det kändes bättre under hela året än det
		gjort tidigare. Våren 1949 tog jag så studenten och det var skönt, när det var över. Jag hade
		verkligen läst mycket och det var en befrielse, när det var klart.
	</p>
	<p class="my-4">
		Några veckor efter studentexamen började jag göra militärtjänst på T4 dvs. Kungliga Skånska
		Trängregementet i Hässleholm. Där trivdes jag mycket bra, framförallt för att vi fick vara ute
		och röra på oss mycket. Någon kväll i veckan hade vi kvällstjänstgöring, annars var kvällarna
		lediga och då kunde jag t.ex. låna böcker på biblioteket och läsa det jag ville.(I motsats till
		tiden före studentexamen) Jag hamnade på ett lastbilskompani, vilket förstås innebar
		lastbilskörning, och det var ju intressant när man var i den åldern.
	</p>
	<p class="my-4">
		På den tiden hade vi ännu inte lediga lördagar, och eftersom vi slutade c:a kl 12 på lördagen
		var det inte lätt att hinna hem till Adelöv och tillbaka igen över veckoslutet. Men som tur var
		hade jag ju då faster Lisa, farbror Manne, och kusinerna Arne och Uno i Malmö, och dit kunde jag
		åka istället. Det var verkligen skönt att få komma dit. Arne hade redan då börjat sällskapa med
		Ulla och vi tre var ute och såg oss om i Malmö emellanåt. Vi var på Malmö stadsteater ibland och
		jag minns särskilt att vi såg "Teaterbåten" med Leon Brunner? som sjöng den djupa basstämman. Vi
		var också på revy Hippodromen någon gång. Där såg jag Gunver Bergkvist för första gången, och
		henne har jag alltsedan dess haft ett gott öga till. Hippodromen, som ju var cirkus från början,
		sedan revylokal och därefter pingstkyrka, men är nu återigen nöjeslokal.
	</p>
	<p class="my-4">
		Efter ungefär nio månader i Hässleholm var vi en hel del, som gick plutonchefsskola i Skövde och
		kom sedan tillbaka till Hässleholm som korpraler. Då fick vi föra befäl över en grupp ibland,
		men jag var också tidvis bilinstruktör dvs lärde de, som inte hade körkort, att köra bil. Det
		var inte något särskilt ansträngande jobb, men det kunde bli tjatigt. Hässleholm var ju inte
		någon stor stad, så jag kunde snart alla stadens gator.
	</p>
	<p class="my-4">
		Militärtjänstgöringen slutade i september 1950, och då kom jag hem till Adelöv igen. Vad jag
		gjorde den närmaste tiden kommer jag inte ihåg, men jag skulle tänka mej att jag arbetade en del
		på Adelövsgård. Den 1 november började jag sedan på Tenhults lantmannaskola, som det hette på
		den tiden. Då började en ganska trivsam tid dels med trevliga kamrater och dels med intressanta
		ämnen. Rektorn, som hette Alf Hermelin, var en mycket speciell man. Han var en bra lärare, men
		han var koleriker och kunde ibland brusa upp för ingenting. Där gick jag först vinterkurs och
		fortsatte sedan med sommarkurs, som slutade någon gång i oktober.
	</p>
	<p class="my-4">
		I Tenhult fanns det också en lanthushållsskola, som hade kurser på både vinter och sommar. På
		sommarkursen började bl.a. Ann-Mari, och jag minns precis när jag såg henne första gången. Hon
		hade varit vid Tenhultssjön och badat tillsammans med några kamrater och när de var på vag
		därifrån kom jag tillsammans med några kamrater. En av mina kamrater hette Gunnar Johansson och
		hans pappa arrenderade prästgården i Öggestorp. Han kände Ann-Mari, sa vi stannade och pratade
		en stund. Hon hade samma cykel, som hon sedan har haft, och hon stod med ett ben på vardera
		sidan av cykeln och jag tyckte då att hon såg trevlig ut. Sedan skulle eleverna på
		lanthushållsskolan och lantmannaskolan spela teater tillsammans och då fick vi tillfälle att
		träffas mera. Snart behövdes det inte någon teater för att vi skulle träffas.
	</p>
	<p class="my-4">
		I slutet av september slutade jag på Tenhult och var hemma i Adelöv ett par veckor. Ann­ Mari
		hade fått plats som au-pair hos en svensk familj i Paris och åkte dit. Hon åkte tåg(sittvagn)
		till Paris. Resan tog tre dygn, sa hon var ganska trött, när hon kom fram. Hennes koffert var
		polleterad på tåget, och det var den spånkofferten, som sedan många år står på andra våningen i
		Holmedal vid utgången till altanen. I mitten av oktober åkte jag till Viad för att börja som
		ladugardselev. Viad ligger i Grödinge socken c:a 1 ½ mil söder om Tumba. Det var rektor Hermelin
		på Tenhult, som hade skaffat mej och två kamrater, som också gått på Tenhult den platsen. Viad
		hörde till Lantbrukshögskolan och där gjordes bl.a. mjölkningsförsök med enäggstvillingar.
		Arbetet började kl. 5 på morgonen och slutade kl. 17.00 med middagsvila däremellan På Viad fanns
		det många elever, sa där fanns ett särskilt elevhushåll. Vi bodde nog en åtta stycken pojkar i
		en bostad, flickorna bodde i huvudbyggningen på tredje våningen. Några av eleverna var tyskar
		och där grundlade jag nog en del av mina tyskakunskaper. En av eleverna spelade dragspel och
		ganska ofta var det dans i vårt hus. På sommaren dansade vi på magasinet. Under vintern arbetade
		jag i ladugården och under sommarhalvåret ute i jordbruket. Jag trivdes bra under hela tiden på
		Viad men bäst var det på sommaren, när jag var med i vårbruk, höskörd m.m. Jag slutade på Viad i
		oktober och åkte då hem till Adelöv.
	</p>
	<p class="my-4">
		Den I nov. åkte jag till Uppsala för att börja på Ultuna. Jag hade fart ett rum hos en trevlig
		ung familj på Lindsbergsgatan. Mannen i familjen hette Bengt Bentzer och gick tredje årskursen
		på Ultuna. Mandagen den 3 nov. började "allvaret" på Ultuna. Då började den s.k. småttingveckan
		med uppfostran av oss småttingar. Det hela började med att vi togs emot utanför biblioteket och
		en av faderbröderna kom i galopp och ställde upp sej och hästen framfor oss. Jag minns inte, om
		han hälsade oss välkomna, men vi skulle i alla fall som vidriga småttingar tagas omhand och
		uppfostras under den kommande veckan. Vi bevistade sedan en föreläsning, som jag inte minns
		något av, men den hölls av en av faderbröderna och var naturligtvis på en mycket hög nivå. Det
		jag minns var att han hade med sej en packe läroböcker säkert en halv meter hög. Innehållet i
		dessa böcker skulle vi inhämta under den närmsta veckan. Vad vi inte visste var att alla
		faderbroder satt i kårlokalerna och genom högtalare kunde åhöra föreläsningen. De hade säkert
		lika roligt som vi hade ett år senare, när vi också kunde åhöra motsvarande föreläsning.
		Därefter samlades vi i kårhusets källare, där vi fick ta av oss skorna. Sedan fick vi gå i en
		mörklagd spiraltrappa upp till själva kårlokalen. Där satt då faderbröderna samlade och under
		diverse burop släpptes vi ut där. Faderbröderna hade då varsin förteckning över oss, där de då
		kunde läsa om vad vi haft för oss tidigare. En del hade då förvrängts, och vi kunde hånas för
		alla dumheter, som vi hade haft för oss. Så småningom skulle vi gå därifrån och upptäckte då att
		skorna, som vi tagit av oss i källaren låg i en stor hög på golvet! Vi fick också varsin napp
		att hängas i ett band om halsen. På kvällarna blev vi hemkommenderade till olika "faderbröder".
		Första kvällen var jag hemma hos faderbroder "Törst", som verkligen gjorde skal för namnet. Där
		var också flera andra småttingar. Följande kvällar var jag hos andra faderbroder. Det var inga
		nyktra tillställningar, men i alla fall ett bra satt att lära känna en del äldrekursare lite
		närmare. På lördagskvällen var det middag på Ultuna med dop av småttingarna och sedan var vi
		fullvärdiga medlemmar av studentkåren. Sedan följde då den ganska trevliga tiden på Ultuna.
	</p>
	<p class="my-4">
		Sommaren 1953 åkte jag till England för att jobba en gård. Garden låg c:a 10 mil norr om London
		i en liten by, som hette Fotheringay inte sa långt från Peterborough. Gården hette Manor Farm
		och lantbrukaren hette Cyril Allday. Jag åkte båt från Göteborg till London och anlände till
		London en morgon. Jag tog sedan tåget upp till Peterborough, där mr Allday matte mej. Vi kom
		till gården på förmiddagen, och på eftermiddagen fick jag börja arbeta, sa det blev inte någon
		lång introduktion. De lade in ensilage och det gjordes i stackar, som var cirkelrunda och jag
		fick gå ovanpå och lägga foder och trampa. Jag var ju rätt sa otränad till en början, sa jag var
		ganska trött på kvällarna. Frukost och kvällsmat åt jag tillsammans med familjen, som bestod av
		herr och fru Allday och en syster till mr Allday. Till frukosten serverades det alltid bacon och
		ägg, smör och bröd och mycket starkt kaffe, som man hällde mycket mjölk i. Lunchen åt jag
		tillsammans med rättaren på gården på den pub, som fanns i byn. Som jag minns fick vi ofta kall
		skinka, majonäs och sallad. På Midsommarafton minns jag att jag längtade hem. Midsommarafton
		firades inte och det mörknade betydligt tidigare an hemma. Jag tänkte förstås på dans kring
		midsommarstången och allt annat firande.
	</p>
	<p class="my-4">
		Mycket var naturligtvis annorlunda i lantbruket mot i Sverige. Bl.a. hängde man hö på s.k.
		trajbods. Det var en träställning som en pyramid med tre ben. Ett par dm från marken var det
		tvärstänger mellan stolparna och på dessa tvärstänger skulle höet hängas. Det var inte sa lätt,
		så ibland åkte en del hö, som jag lagt upp, ned på marken. Då kom mr Allday och skrek "You must
		not make silage" dvs du far inte göra ensilage.
	</p>
	<p class="my-4">
		Rättaren på gården var tyskfödd, men hade kommit till England långt före kriget. Han hade gjort
		militärtjänst i England. Eftersom han kunde tala tyska blev han före invasionen i Normandie
		tillsammans flera andra överskickad till Normandie för att rekognosera. Jag frågade honom hur
		det hade varit. Han berättade inte sa mycket om det, men han sa att det hade varit "thrilling".
	</p>
	<p class="my-4">
		TV:n hade gjort sin entré i England, men det var precis i början, sa min värdfamilj hade ingen
		TV, utan vi fick gå till grannen och titta. Likadant var det ju i Sverige, när TV:n var ny. Jag
		minns dock att jag inte både sa stor behållning av TV-tittandet för jag hade varit ute och
		jobbat på dagarna, sa jag var ganska trött och nickade redan då till framfor TV:n.
	</p>
	<p class="my-4">
		Över veckosluten åkte jag ibland till London. Släktingar till en familj i Adelöv bodde i London
		och jag fick åka ned och hälsa på dom och samtidigt se mej om i London. Det var mycket trevligt
		att på egen hand kunna utforska en del av den stora staden och verkligen kunna se att bar fanns
		folk från jordens alla hörn. Jag kom till England en vecka efter kröningen av drottning
		Elisabeth och i alla skyltfönster skyltades det med fotografier av henne och prins Philip. Det
		hade ju inte gått sa många år sedan krigsslutet, sa det fanns mycket, som ännu inte var
		uppbyggt, och det fanns bombkratrar lite har och där. Mitt värdfolk bodde i en våning med tre
		plan. Den hade varit hopbygd med ett mycket större hus, men resten av huset var bortbombat.
	</p>
	<p class="my-4">
		I början av augusti åkte jag hem. Då åkte jag båt från Harridge till Esbjerg och sedan tag till
		Köpenhamn. När jag kom till Malmö övernattade jag hos faster Lisa och nästa dag åkte jag hem
		till Adelöv.
	</p>
	<p class="my-4">
		I slutet av augusti 1953 kom Ann-Mari till Uppsala och började på den förberedande kursen på
		Fackskolan för Huslig Ekonomi. Den pågick det läsåret och hösten 1954 började hon på den
		ordinarie utbildningen på Facks, som det kallades i dagligt tal. Under den har tiden bodde vi
		inackorderade på olika ställen(var för sej), men våren 1956 skaffade vi en lägenhet på
		Byggmästargatan i det då helt nybyggda området Salabacke.
	</p>
	<p class="my-4">
		Fredagen den 8 juni 1956 tog Ann-Mari examen på Facks. Tant Märta, farbror Enock och mamma var i
		Uppsala då. Ann-Mari och "gästerna" åkte båt till Skokloster på lördagen. Tyvärr kunde jag inte
		åka med, för jag gjorde examensarbete. Farbror Enock åkte tag hem på måndagen och vi övriga
		följde efter i bil på tisdagen.
	</p>
	<p class="my-4">
		Samma sommar den 14 juli gifte vi oss. Vigseln förrättades i Öggestorps kyrka av kyrkoherde
		Gustav Skiöld från Adelöv. Efteråt var det middag på restaurang Backagården, som ligger utefter
		gamla vägen mellan Jönköping och Huskvarna nere vid Vättern. Den s.k. bröllopsnatten
		tillbringade vi på Park Hotell i Jönköping. Dagen efteråt åt vi middag hos Ingrid och Harald.
		Sedan åkte vi bröllopsresa till Halmstad, Göteborg, Ängön, där Ann-Mari sommarjobbat, till
		Uddevalla, Dalsland, Alster i Värmland, Askersund och sedan hem till Adelöv. Då hade vi varit
		borta en vecka. Därefter var vi omväxlande i Rommelsjö och Adelöv och den 6 augusti åkte vi
		tillbaka till Uppsala
	</p>
	<p class="my-4">
		Tisdagen den 14 aug. kom Pappa och Morfar med tåg till Uppsala. Vi visade dom runt i Uppsala,
		t.ex Domkyrkan, Ultuna och Linnes Hammarby. Vi var en dag i Stockholm och tittade på Slottet och
		Skansen. Måndagen den 20 aug. körde jag Pappa och Morfar hem. Vi stannade till hos Oscar
		Johansson i Skarpåker i Runtuna socken utanför Nyköping och drack kaffe. Oscar var kusin till
		Mormor. På tisdagen åkte jag tag tillbaka till Uppsala.
	</p>
	<p class="my-4">
		Under hösten fortsatte jag att läsa och Ann-Mari hade en del kvällskurser och diverse vikariat.
		Den 18 dee. åkte vi hem till Småland. Vi var i Forserum på julottan och hörde Eilerts Fryland
		predika. Efteråt drack vi kaffe hos Eilerts och Haralds föräldrar. Jag var sedan i Adelöv, men
		åkte till Rommelsjö på nyårsafton och var sedan där ett par dagar. Måndagen den 7 januari 1957
		åkte Ami och jag tillbaka till Uppsala. Jag fortsatte att läsa som vanligt. Jag ser i gamla
		dagböcker att vi hade mycket umgänge med kurskamrater. Ami och jag brukade någon gång under
		vintern promenera ut till G:a Uppsala. Detta året gjorde vi det den 17 mars och fikade då på
		Odinsborg. Fikat bestod mestadels av choklad och vispgrädde och en smörgås.
	</p>
	<p class="my-4">
		Den 25 maj åkte jag hem till Adelöv. Nästa dag, som var en söndag åkte Mamma, Pappa och jag per
		bil till Uppsala. Jag minns att Pappa rätt snart ville att vi skulle stanna för att ta en
		kaffepaus, men det tyckte inte jag. Så har efteråt ångrar jag att jag inte gjorde honom till
		viljes. Anledningen till att vi åkte till Uppsala var att Pappa fyllde 70 år den 29 maj. På
		kvällen åt vi middag på Flustret. På fredagen var vi i Stockholm och på lördagen åkte vi hem
		till Adelöv. Nästa dag åkte jag tag tillbaka till Uppsala. Jag minns Pappa som ganska trött på
		resan. Han fick en hjärnblödning 1951, men efter den blev han ganska kry igen. Han fick en
		hjärnblödning till kanske var det före Uppsala-resan. Han dog i augusti 1958 ungefär två månader
		efter att Carin föddes. När jag ringde hem och talade om att Carin var född minns jag att det
		var han som svarade. För att återgå till år 1957, sa tillbringade vi större delen av sommaren i
		Småland, dels i Adelöv och dels i Rommelsjö. Vi åkte tillbaka till Uppsala den 10 augusti.
		Hösten fortgick sedan med läsning för min del och med tjänstgöring på olika ställen för
		Ann-Mari. Jag ser i dagboken att Fritzells var i Uppsala och hälsade på.
	</p>
	<p class="my-4">
		Sedan blir det ett hopp till den 9 juni 1958. Då körde jag Ann-Mari till KK någon gång på em.
		Jag stannade kvar hos henne till kl. 4 nästa morgon. Jag åkte tillbaka till henne någon gång på
		f.m. nästa dag. Kl 13.21 föddes Carin, som vägde 3490 g och var 52 cm lång. Det var en stor dag.
		Inte förrän den 17 juni kom Carin och Ann-Mari hem. Någon gång i augusti blev jag färdig med
		agronomexamen.
	</p>
	<p class="my-4">
		Min första tjänstgöring hade jag sedan på St. Segerstads lantmannaskola i Reftele i närheten av
		Värnamo. Det var på en 6-veckorskurs och under tiden var Ann-Mari och Carin i Rommelsjö. Efter
		detta åkte vi tillbaka till Uppsala igen. Jag hade inget arbete då, men på Ultuna fanns det en
		anslagstavla med lediga jobb, som jag studerade varje dag. Där kom upp ett jobb i Medelpad på
		Hussborgs skogs- och lantmannaskola. Jag sökte och fick tjänsten, som varade ett halvår. Vi åkte
		bil upp med mycket packning och Carin liggande i vaggan i baksätet. Vi fick hyra en möblerad
		lägenhet i själva skolhuset
	</p>
	<p class="my-4">
		Rektorn där hette Manne Nordenborg och där fanns en lärare, som hette Ragnar Laestadius. Bada
		två var original och satte sin prägel på skolan. Ragnars bror Lars Levi var chef på Stockholms
		stadsteater.
	</p>
	<p class="my-4">
		På skolan fanns en bastu, som vi utnyttjade. Den var i ett hus, som bara värmdes upp en gång i
		veckan, när vi skulle bada bastu. I omklädningsrummet var det trägaller på golvet och under
		gallret var det is. Det fanns elektrisk belysning där, med det fanns också en stor öppen spis,
		sa vi släckte belysningen och hade elden i öppna spisen som belysning. Bastun var förstås
		vedeldad. Sa förflöt vintern 1958-1959, och när vinterkursen på Hussborg var slut var också min
		tjänst där slut. Vi återvände då till lägenheten i Uppsala, och jag letade efter ny tjänst. I
		tidningen Lantmannen hade jag last om en ny lantmannaskola i Södermanland, som hette Stenkvista,
		och som såg trevlig ut på bild. Där utannonserades en tjänst i husdjurslära, som jag sökte och
		också fick. Jag var där och tittade på skolan en gång innan vi flyttade dit och träffade då
		förstås rektor Henrie Bergström(senare Blix) samt även Bengt och Lars-Erik, som då bada jobbade
		där. Efter att ha varit på rektorsexpeditionen följde jag med Lars-Erik till Gula huset, där han
		då bodde. Där träffade jag också Birgitta och Vania och blev val mottagen av alla. Birgitta var
		då gravid och precis lika långt kommen, som Ann-Mari. Sa småningom föddes då Eva, den 25 juni
		och Arvid den 28 juni. Men dessförinnan hade vi flyttat till Stenkvista, jag några dagar i
		förväg, och sedan kom Ann-Mari med Carin i flyttbussen. Flyttbussen hade kört fel och kört om
		Örsta och kom alltså den krokiga vägen hit, sa Ann-Mari mådde illa. Jag minns att det stora
		rummet i lägenheten verkligen kändes stort, så vi fick ställa vara få möbler glest för att det
		inte skulle se alltför ödsligt ut.
	</p>
	<p class="my-4">
		Den 25 juni föddes Eva. Mamma var hos oss då och på natten till den 25 juni åkte Ann-Mari och
		jag in till lasarettet och Eva föddes på förmiddagen. Ännu en stor händelse i familjelivet.
	</p>
	<p class="my-4">
		Sommaren 1959 var en mycket varm och torr sommar. Ibland åkte vi till Borsten och badade och jag
		minns att vi var tvungna att ha en gardin över Evas bag, sa att hon inte skulle bli
		getingstucken. På Stenkvista hade vi en trevlig tid. Där fanns då min kurskamrat Lars-Erik med
		Vania och Bengt och Birgitta. Vi träffades ofta på kvällarna ibland tillsammans med andra av de
		yngre på skolan. Lantmannaskolorna på den tiden var som ett eget litet samhälle och personalen
		till en del som en stor familj. Alla födelsedagar firades med kaffe kl. 3, så det blev många
		sådana träffar. På den tiden var Henrie Blix(som tidigare hette Bergström) rektor och han och
		hans fru Märta var noga med detta med födelsedagar. Henrie var en mycket positiv människa och
		mottaglig för nya impulser.
	</p>
	<p class="my-4">
		På hösten 1960 blev husdjurstjänsten på Ulfhäll ledig och jag sökte och fick den. Det var en
		annorlunda skola med mera inriktning på det praktiska arbetet. Rektor där hette Sigge Kvarnback
		och var en mycket fordrande men kunnig man, som hade stor respekt med sig. Efteråt kan man tycka
		att han var för krävande, men man lärde sej mycket under den tiden. Vi bodde i en mycket vacker
		lägenhet med utsikt över Mälaren
	</p>
	<p class="my-4">
		Första året fanns det ingen ordentlig ladugård p.g.a. att den gamla hade brunnit en månad innan
		vi kom dit. Det var en stor brand med 150 djur innebrända. Den nya ladugården byggdes vid
		Gorsingeholm och blev färdig kommande höst dvs hösten 1961. Ladugardsforestånare var Gottfrid
		Nilsson, som var en kunnig man men minst sagt hetlevrad. Ilskan gick dock fort över. Jag var
		ofta ute i ladugården på morgnarna före frukost för att "kolla läget". På somrarna mjölkades
		inte korna i ladugården utan vid en utemjölkningsanläggning, där också mjölktanken ställdes upp
		sommartid. Tankbilen körde alltså ut dit och hämtade mjölken.
	</p>
	<p class="my-4">
		På Ulfhäll var det mycket studiebesök och det var alltid roligt att visa besökarna runt. Min
		uppgift var ju oftast att visa ladugård och svinhus. Ulfhäll hade en avelsbesättning på
		svinsidan, sa det kom många bönder dit för att köpa galtar eller gyltor(ungsuggor). Det kom
		också köpare betydligt längre ifrån bl.a. Japan och Ryssland för att köpa avelsdjur. Det hörde
		också till den roligare delen av arbetet. Jag minns att jag berättade för någon japan hur vi
		utfodrade grisarna och det var han mycket intresserad av. Jag talade också om hur vi gjorde för
		att det skulle gå pt sa lite arbetskraft som möjligt. Men det var han inte alls så intresserad
		av, för i Japan fanns det gott om arbetskraft
	</p>
	<p class="my-4">
		Sigge Kvarnbäck hade slutit ett avtal med bönder på Selaön om att köpa smågrisar därifrån.
		Avtalet innebar att de lantbrukare, som hade smågrisproduktion fick sälja sina smågrisar till
		Ulfhäll till ett pris per kg, som fanns angivet i tidningen Land för varje vecka. När bönderna
		hade smågrisar lagom stora för försäljning ringde de till gårdskontoret och talade om hur många
		grisar de hade klara. Det ankom då på husdjursläraren att ta sin bil och åka ut till dessa
		bönder. Samtidigt körde en elev med traktor och vagn med svinburar på ut till gårdarna.
		Smågrisiga vägdes och vikterna antecknades i liten bok och påfördes varje lantbrukare. När
		samtliga grisar var hämtade åkte vi hem med "dagens skörd". Sedan ankom det på bokhållaren att
		rakna ut betalningen för grisarna och skicka pengarna till lantbrukarna. Det var en trevlig del
		arbetet på Ulfhäll, eftersom man lärde känna många bönder på Selaön. Det var också ett bra satt
		att lära sej en hel del om svinskötsel.
	</p>
	<p class="my-4">
		På nedervåningen i samma hus som vi bodde växtodlingsläraren. Det var till att börja med Birger
		Andersson med sin fru Margareta. De hade inga egna barn då, så särskilt Margareta lekte mycket
		med Carin och Eva, som var två resp. ett år, när vi kom dit. Vi träffades ofta på kvällarna på
		en kopp kaffe eller något att äta. Birger blev så småningom rektor på Osby (Sala)
		lantbruksskola. Sedan var han kommunalpolitiker för centerpartiet i Sala och därefter
		riksdagsman några år. Margareta var lärare i Sala och sedan i Västerås. De tog en fosterson och
		fick senare också en egen son. Birger och Margareta bor numera i Uppsala.
	</p>
	<p class="my-4">
		Birger efterträddes sa småningom som växtodlingslärare på Ulfhäll av Olle Hammar. Då kom förstås
		också Bodil och deras Johan. Vi träffades också ofta på kvällarna.
	</p>
	<p class="my-4">
		Vi träffade också alltemellanåt Bengt och Birgitta ock Lars-Erik och Vania. Jag såg i dagboken
		att vi åkte till Lars-Erik och Vania lördagen den 27 jan. 1962. Strax före Malmköping kom vi dvs
		Ann-Mari på att vi hade glömt flickornas kappor hemma. Då var det bara att vända och åka
		tillbaka och hämta dom. Då hade inte Lars-Erik och Vania flyttat till Knutsta, utan de bodde i
		en lägenhet i Valla. Carin vaknade kl. sex på söndagsmorgon och Lars-Erik och jag fick av någon
		anledning frukost på sängen!
	</p>
	<p class="my-4">
		Lördagen den 13 juli 1963 åkte Ami och jag till Småland för att vara med på Tores och Ingers
		bröllop i Örserum. I Adelöv hämtade vi Mamma och Morfar. Vigseln var i Örserums kyrka och
		middagen på Orensbaden restaurang. Ami och jag var värdfolk på bröllopet. Efteråt var vi med
		till Uppgränna. Som vanligt hade det samlats en hel del folk för att titta på brudparet. Man
		ropade "brudparet ut" och då fick brudparet komma ut och gå en runda och visa upp sej. Jag
		förmodar att det bjöds på pepparkakor då.
	</p>
	<p class="my-4">
		Lördagen den 27 juli 1963 kom tant Märta och farbror Enock till Ulfhäll. På söndagen var vi ute
		och åkte bil och besökte bl.a. Sparreholm, Stenhammar och Harpsund. På måndagen fyllde Ami 30
		år. Hon fick förstås kaffe på sängen, och på kvällen var det många från Stenkvista, som kom och
		gratulerade.
	</p>
	<p class="my-4">
		Tjänsten på Ulfhäll var inte ordinarie, och därför letade efter en sådan och på Ingelstad två
		mil söder om Växjö annonserades det ut en ordinarie tjänst. Vi dvs. Ann-Mari, Carin, Eva, Johan
		och jag åkte ned Ingelstad och tittade på skolan och träffade bl.a, rektorn där, som hette Kuno
		Melander. Han visade oss runt och visade förstås den bostad, som vi skulle få, om vi flyttade
		dit. Bostaden såg bra ut och det övriga också. Jag sökte och fick platsen, och i slutet av
		oktober flyttade vi dit.
	</p>
	<p class="my-4">
		Ingelstadskolan var i många avseenden mycket olik Ulfhäll, och trakten var också mycket olik
		landskapet kring Strängnäs. Här var skolan verkligen en by för sej. Vi var ofta bjudna till
		rektorns och även till ekonomiläraren Gösta Karlström och hans fru Aina. Min kurskamrat Bengt
		Rydstrand var växtodlingslärare på skolan och hos honom var vi också förstås. Han var inte gift,
		när vi kom dit, men där fanns också skolans husmor Karin, som sa småningom blev fru Rydstrand.
		Både Karin och Bengt var mycket barnkära, sa barnen var ofta hos dem. Kuno Melander var en
		mycket speciell person, som kunde hallå mycket långdragna tal och föredrag. Han var snäll, men
		var en mycket dålig organisator. På sommaren skulle t.ex. personalen träffas varje kväll på
		rektorsexpeditionen och göra morgondagens schema. Det var verkligen en stor omställning att
		komma från Sigge Kvarnbäck på Ulfhäll till Kuno Melander. Till de stående inslagen hörde som på
		de flesta andra lantbruksskolor julfesten med en massa god mat. Vid dessa tillfällen var också
		personalens familjer inbjudna, så det samlades mycket folk. Barnen uppskattade också dessa
		fester, för då kom förstås alla lekkamrater, som fanns på skolan. De åt ju snart färdigt, och då
		blev det ett väldigt springande i korridorerna.
	</p>
	<p class="my-4">
		Kronobergs län skilde sej ur jordbrukssynpunkt mycket från Södermanlands län. Har fanns mera
		skog, och framförallt var gårdarna mycket mindre. Även här (liksom på Stenkvista­ skolan) hade
		vi s.k. hemkontakt, vilket innebar att lärarna på sommaren åkte ut till de av vinterkursens
		elever, som ville vara med i hemkontakten. De flesta eleverna åkte tillbaka till sina hemgårdar,
		men en del hade tagit anställning på andra gårdar, och då kunde vi också besöka dessa. Dessa
		besök var mycket lärorika inte minst för oss lärare. Dels fick vi komma till olika delar av
		länet, och dels fick vi se hur det såg ut på de olika gårdarna och diskutera med både elever och
		deras fader eller arbetsgivare. En del gårdar var verkligen små, men jag tyckte att alla bönder
		trots små inkomster trivdes med tillvaron, fast det var ett slitsamt liv. Man levde nära
		naturen, och under mindre intensiva årstider kunde bonden efter morgonmjölkningen ta geväret och
		gå ut i skogen och jaga hare e.dyl. Hjälpsamheten mellan människor var stor, och ingen var
		former an någon annan.
	</p>
	<p class="my-4">
		När rektor Kuno Melander gick i pension efterträddes han av sin svåger Gösta Karlström, som var
		ekonomilärare på skolan. Han hade kommit till skolan som ungkarl, men träffade där Britta
		Melanders syster Aina, som han sa småningom gifte sej med. Aina var änka efter en av de flygare,
		som omkom i samband med nedskjutningen av en DC 3:a över Östersjön. Hans plan var ute för att
		söka efter det nedskjutna planet, och då tog ryssarna också det planet. Aina hade alltid glimten
		i ögat och var en stor humorist. Gösta var också mycket trevlig.
	</p>
	<p class="my-4">
		Vad hände annars under under Ingelstadstiden? Den största händelsen inträffade den 7 april 1965,
		då Anna föddes. Karin Rydstrand(som då inte hette Rydstrand) var vidtalad att ta hand om barnen,
		när Ami och jag skulle åka in till BB. Den dagen, då det blev akut att åka in sade jag till
		Karin på morron att vi skulle åka. Hon trodde det var väldigt bråttom, men vis av erfarenheten
		kunde jag lugna henne att det inte var fullt sa bråttom. Vi kom i alla fall i väg lite senare på
		morron och på förmiddagen föddes Anna. Det upptäcktes rätt snart, att Anna hade gulsot och
		därför fick hon inte följa med hem meddetsamma utan stanna kvar på lasarettet tills gulsoten
		hade försvunnit. Då minns jag att Ann-Mari inte var glad, hon tyckte att det kändes som om en
		kroppsdel hade ryckts bort. Desto roligare blev det sedan när Anna fick komma hem. Hon var ett
		gladlynt litet barn och jag minns, när hon blev lite äldre och kunde sitta på golvet omgiven av
		kuddar och vifta med armarna och skratta.
	</p>
	<p class="my-4">
		Förutom folk på skolan umgicks vi med två familjer, som bodde i Växjö. Det var dels Ing­ Mari
		och Per Sundling, som hade två fickor. Ing-Mari var kurskamrat med Ann-Mari på Fackskolan och
		Per jobbade på Skandinaviska banken i Växjö. Den andra familjen var Stina och Nils Walfridsson.
		Båda var från Adelöv, Stina, som var jämnårig med mej, var dotter till smeden Ernst Åström, som
		bodde vid Adelövsgård och Nils var ifrån Råssås nära Platsen. Han var elingenjör och jobbade på
		Fläktfabriken i Växjö. De hade två pojkar och en flicka.
	</p>
	<p class="my-4">
		Hur kunde en jul firas hos oss? Förberedelserna för julen började förstås en bit före jul med
		städning, julklappsinköp och matlagning. Kvällen före julafton, när det mesta var färdigt hemma
		gick vi och badade bastu på skolan. Enligt korta anteckningar i en kalender för år 1968 började
		julaftonen med att barnen fick julhögar. Kl. 12 doppade vi i grytan. Tomtar var Johan och jag,
		och vi delade ut julklapparna kl. 4. Farmor kom till oss ett par dagar före julafton och hon och
		jag var i julottan. Frylands och tant Marta, farbror Enock, Per-Olof och Bertil kom på juldagen.
		Vi åt väl julmat som vanligt då, men de stannade inte länge för dels hade Katarina
		öroninflammation och dels skulle Ströms hem och mjölka. Söndagen den 29 dee. åkte vi till
		Öggestorp. Först drack vi kaffe hos Frylands och sedan åkte vi till Rommelsjö. Där åt vi julmat
		(mycket mat som vanligt). Senare var det julklappsutdelning med Johan och Anders som tomtar.
		Måndagen den 30 dee. åkte vi först till Huskvarna och fotograferade barnen och sedan fortsatte
		vi till Adelöv. Nästa dag dvs nyårsafton var Ann-Mari och jag i Gränna och handlade. På kvällen
		var barnen och jag uppe till kl. 10.
	</p>
	<p class="my-4">
		Vi var också ganska ofta i Öggestorp över nyår. Då hade vi julklappsutdelning i Rommelsjö och vi
		åt julmat där också.
	</p>
	<p class="my-4">
		Torsdagen den 16 januari 1969 var det julgransplundring hemma hos oss. Ann-Mari hade den
		tillsammans med Rut Nilsson. Jag visade film och var tomte.
	</p>
	<p class="my-4">
		Jag trivdes ganska bra i Ingelstad, men ville i alla fall söka något annat. Sommaren 1970
		annonserades det ut en ordinarie husdjurslärartjänst på Stenkvistaskolan, som jag sökte. Jag
		fick tjänsten och vi flyttade dit i slutet av oktober 1970. En flyttfirma från Alvesta körde
		bohaget, och vi åkte till Knutsta och övernattade hos Lars-Erik och Vania. Jag minns att jag
		tyckte det kändes konstigt att inte ha någon bostad, och att alla möbler och annat var på vägen.
		Men bostad fick vi, och den var inte okänd för oss, eftersom det var samma hus, som vi hade bott
		i tio år tidigare. Vi blev val mottagna, eftersom vi kände de flesta, som arbetade på skolan.
		Barnen fick börja i Hällberga skola, och det är val inte så lätt att att börja i en ny skola.
		Men jag tror att lärarna i de klasser, som barnen hamnade i hade förberett de blivande
		kamraterna väl.
	</p>
	<p class="my-4">
		Vid den tiden hade vi också häst, en New Forest-pony, som hette Majard. Det var Carin, som red
		på honom, och han var inte den lättaste att tas med, för han hade ett mycket hett temperament.
		Han kom till Stenkvista med en hästtransport mitt i natten. Bengt hade ordnat med att det fanns
		en ledig box i ungdjursstallet i ladugården. Jag hade ställt klockan på ringning klockan ett och
		när jag kom ner till ladugården stod bilen redan där, så det var bara att ta in Majard i
		stallet.
	</p>
	<p class="my-4">
		I Stenkvista började en ny period i livet. Men tack vare att vi bott i Stenkvista tidigare
		(1959-1960) kände vi oss ganska hemma med skolan och omgivningarna. Rektor Henrie Blix var en
		lättsam chef, om an något okoncentrerad ibland. Lindens kände vi ju sedan tidigare, och ibland
		åkte vi också ner till Knutsta i Lerbo och hälsade på Lars-Erik och Vania.
	</p>
	<p class="my-4">
		Sommaren 1971 gjorde vi en Tysklands-resa. Första kvällen tillbringade vi i Göteborg på ett
		regnigt Liseberg. På natten åkte vi färja till Kiel och fortsatte dagen därefter till en liten
		by söder om Hamburg, där vi låg på ett Gasthaus. Vi fortsatte sedan till Bayern, där vi
		tillbringade några dagar. Vi tältade mestadels, och jag minns särskilt en campingplats, där vi
		på morgonen vaknade vid ljudet av koskällor. Hemvägen gick över Köpenhamn, Helsingör,
		Hälsingborg, och Ingelstad, där vi övernattade hos Rut och Stig.
	</p>
	<p class="my-4">
		Den 2 augusti skrev vi kontrakt med Alf Lundin angående husköp till ett pris på 175 000:-. Det
		har vi aldrig behövt ångra. Den 30 augusti kom snickaren och började göra om i köket. Ami och
		jag började så småningom göra om i stockarummet. Lördagen den 25 september flyttade vi med hjälp
		av Bengt och Rolf från lärarbostaden till Holmedal. Flyttningen var klar klockan tre, och då
		bjöds vi på kaffe och våfflor hos rektorn. En hel del arbete återstod naturligtvis i huset som
		tapetsering, målning m.m. men i slutet av oktober var det mesta klar. Julen 1971 firades i
		Stenkvista, men till nyår åkte vi till Småland. Fredagen den 14 januari 1972 dansade vi ut
		granen hos Lindens. Någon, som ofta omnämns i dagboken är Majard. Han skulle ju skötas, ibland
		gjorde jag det och ibland Carin. Carin var förstås ofta ute och red på honom. Jag badade i
		poolen redan den 14 maj, vilket jag nu tycker verka vara mycket tidigt.
	</p>
	<p class="my-4">
		Den 14 juli hade vi packat bilen och åkte kl. 6 mot Norge. Vi åkte över Kongsvinger och Elverum
		till Bakken-Berg. Sedan både vi det bra där, vandrade i fjällen, åt våfflor i våffelstugor, åkte
		till Grimsdalshytta, till Folldal m.fl. ställen. Tisdagen den 18 juli packades bilen åter och vi
		vande hemåt över Folldal, Alvdal, ldre, Mora, Borlänge och sa småningom Stenkvista. Fredagen den
		21 juli åkte vi till Adelöv och åt jordgubbar på kvällen hos Farmor. Sedan växlade vi mellan
		Adelöv, Rommelsjö och Öggestorp. Vi var med och körde hö i Rommelsjö under två dagar. Onsdagen
		den 3 aug. åkte vi hem till Stenkvista. Julen 1972 firade vi i Stenkvista, men dagen före
		nyårsafton åkte vi till Småland.
	</p>
	<p class="my-4">
		Lördagen den 7 april 1973 uppvaktades Anna på sin 8-arsdag med ett skrivbord. På söndagen den 8
		april står det i dagboken att Carin, Johan och Anna hade vattenkoppor. Den 27 juni åkte vi till
		Småland och den 6 juli kom vi tillbaka. Måndagen den 9 juli åkte vi till Norge. Frylands var då
		redan där. Lördagen den 21 åkte vi hem.
	</p>
	<p class="my-4">
		Måndagen den 1 juli 1974 åkte Carin och Eva moped från Stenkvista till Adelöv. Vi både trott att
		de skulle övernatta på vägen, men det behövde de inte.
	</p>
	<p class="my-4">
		Nu blir det ett raskt sprang till lördagen den 1 februari 1975. Då åkte vi till Öggestorp och
		träffade Bill och Marion, som kommit till Sverige. På söndagen var vi först på Öggestorps
		kyrkogård. Sedan åkte vi till Brahehus för att se på utsikten. Men det var tät dimma, men de
		fick i alla fall se Brahehus. Sedan körde vi över Gränna till Gyllene Uttern, där vi åt en god
		middag med sandwich, kalvfilé och moccaglass. Vi åkte därefter direkt hem. Lördagen den 21 juni
		åkte vi till Kolmårdens djurpark, där vi mötte Frylands och Ströms. Vi körde genom Safariparken.
		Det var en varm dag. Den sommaren var vi också i Småland och i Norge. Den 29 juli firade vi Amis
		42-årsdag. Hon fick en kamera och strumpsticksetui. (Säkert inköpt i Norge) På em var det kaffe
		i trädgården för skolans folk. Senare kom Lars-Erik och Vania.
	</p>
	<p class="my-4">
		Lördagen den 13 dee. lussade våra barn för oss och för några andra på skolan. Det var julmarknad
		i Hällberga. Vi vakade in det nya året hos Lindens.
	</p>
	<p class="my-4">
		Måndagen den 23 febr. 1976 startade vi kl.5 på morgonen mot Hallen. Vi hade frukost med oss som
		vi intog någonstans under vägen. Kl 14.30 var vi i Hallen och hämtade nyckel hos byggmästare
		Olofsson och fortsatte sedan till vart hus, som inte var vackert, men som var väldigt bra att bo
		i. Första natten stormade det ordentligt och jag var vaken många gånger och trodde att huset
		skulle blåsa iväg. Veckan blev dock bra med mycket sno och mycket skidåkning. Det var mycket
		trafik på hemvägen och en del avkörningar, men för oss gick det bra.
	</p>
	<p class="my-4">
		Måndagen den 7 juni 1976 åkte Carin till Paris. Hon skulle jobba hos en familj där. Söndagen den
		8 augusti hämtade vi Eva i Nynäshamn efter att hon varit på Gotland och Carin i Stockholm efter
